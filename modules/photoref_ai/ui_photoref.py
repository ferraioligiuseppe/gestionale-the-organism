def _token_secret():
    return str(st.secrets.get("privacy", {}).get("TOKEN_SECRET", "fallback_secret")).encode("utf-8")


def _ensure_link_table(conn):
    cur = conn.cursor()
    cur.execute("""
        CREATE TABLE IF NOT EXISTS photoref_link_token (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            paziente_id BIGINT NOT NULL REFERENCES pazienti(id) ON DELETE CASCADE,
            token_hash TEXT NOT NULL UNIQUE,
            created_at TIMESTAMP DEFAULT now(),
            expires_at TIMESTAMP,
            used_at TIMESTAMP
        )
    """)
    conn.commit()


def genera_link_cattura_mobile(conn, paziente_id: int, base_url: str, ttl_minutes: int = 30) -> str:
    """Crea un link mobile monouso valido ttl_minutes, senza login."""
    _ensure_link_table(conn)
    token = _sec.token_urlsafe(24)
    token_hash = hmac.new(_token_secret(), token.encode("utf-8"), hashlib.sha256).hexdigest()
    now = datetime.datetime.utcnow()
    exp = now + datetime.timedelta(minutes=ttl_minutes)
    cur = conn.cursor()
    cur.execute(
        "INSERT INTO photoref_link_token (paziente_id, token_hash, created_at, expires_at) VALUES (%s,%s,%s,%s)",
        (paziente_id, token_hash, now.isoformat(), exp.isoformat()))
    conn.commit()
    return f"{base_url.rstrip('/')}/?photoref_token={token}"


def verifica_token_cattura(conn, token: str):
    """Ritorna paziente_id se il token è valido e non scaduto/usato, altrimenti None."""
    if not token:
        return None
    token_hash = hmac.new(_token_secret(), token.encode("utf-8"), hashlib.sha256).hexdigest()
    cur = conn.cursor()
    cur.execute("SELECT paziente_id, expires_at, used_at FROM photoref_link_token WHERE token_hash=%s", (token_hash,))
    row = cur.fetchone()
    if not row:
        return None
    pid, exp, used = (row.get("paziente_id"), row.get("expires_at"), row.get("used_at")) if hasattr(row, "get") else (row[0], row[1], row[2])
    if used:
        return None
    try:
        exp_dt = exp if isinstance(exp, datetime.datetime) else datetime.datetime.fromisoformat(str(exp))
        if datetime.datetime.utcnow() > exp_dt:
            return None
    except Exception:
        pass
    return pid


def marca_token_usato(conn, token: str):
    token_hash = hmac.new(_token_secret(), token.encode("utf-8"), hashlib.sha256).hexdigest()
    cur = conn.cursor()
    cur.execute("UPDATE photoref_link_token SET used_at=%s WHERE token_hash=%s",
                (datetime.datetime.utcnow().isoformat(), token_hash))
    conn.commit()


def _ensure_table(conn):
    cur = conn.cursor()
    cur.execute("""
        CREATE TABLE IF NOT EXISTS photoref_sessioni (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            paziente_id BIGINT NOT NULL REFERENCES pazienti(id) ON DELETE CASCADE,
            data_sessione DATE,
            stima_od REAL, stima_os REAL,
            asimmetria REAL,
            note TEXT,
            creato_il TIMESTAMP DEFAULT now()
        )
    """)
    conn.commit()


def _salva(conn, paz_id, data_s, stima_od, stima_os, asimmetria, note):
    cur = conn.cursor()
    cur.execute(
        """INSERT INTO photoref_sessioni (paziente_id, data_sessione, stima_od, stima_os, asimmetria, note)
           VALUES (%s,%s,%s,%s,%s,%s)""",
        (paz_id, data_s, stima_od, stima_os, asimmetria, note))
    conn.commit()


def _storico(conn, paz_id):
    cur = conn.cursor()
    cur.execute("""SELECT data_sessione, stima_od, stima_os, asimmetria
                   FROM photoref_sessioni WHERE paziente_id=%s ORDER BY data_sessione DESC""", (paz_id,))
    return cur.fetchall()


def _analizza_riflesso(img: "Image.Image", occhio: str):
    """Stima grezza da luminosità/asimmetria del crescent nel riflesso pupillare.
    Placeholder scientificamente onesto: converte la posizione/ampiezza del
    riflesso chiaro rispetto al centro pupilla in una stima approssimativa (D).
    Va tarato con foto reali; qui resta un valore indicativo, non clinico."""
    arr = np.asarray(img.convert("L"), dtype=float)
    h, w = arr.shape
    cy, cx = h // 2, w // 2
    r = min(h, w) // 4
    region = arr[max(0, cy - r):cy + r, max(0, cx - r):cx + r]
    if region.size == 0:
        return 0.0
    thresh = region.mean() + region.std()
    bright = region > thresh
    ys, xs = np.where(bright)
    if len(xs) == 0:
        return 0.0
    offset_x = (xs.mean() - region.shape[1] / 2) / (region.shape[1] / 2)
    stima_D = round(offset_x * 4.0, 2)  # scala arbitraria ±4D, da calibrare
    return stima_D


