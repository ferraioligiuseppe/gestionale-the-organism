def render_photoref(conn, paz_id: int, paziente: dict = None) -> None:
    st.subheader("📸 Photoref AI — Stima refrazione")
    st.caption("Screening qualitativo da foto del riflesso pupillare (tipo photoscreener). "
              "Non sostituisce autorefrattometro, schiascopia o refrazione soggettiva.")
    if not _HAS_DEPS:
        st.error("Dipendenze mancanti (pillow, numpy) — aggiungerle a requirements.txt per attivare l'analisi.")
        return

    try:
        _ensure_table(conn)
        _ensure_link_table(conn)
    except Exception as e:
        st.error(f"Impossibile preparare la tabella: {e}")
        return

    st.markdown("##### 📱 Genera link cattura mobile (guidata, con flash/zoom)")
    base_url = st.text_input("URL pubblico del gestionale", "https://theorganism.com", key="pr_base_url")
    ttl = st.number_input("Validità link (minuti)", 5, 180, 30, 5, key="pr_ttl")
    if st.button("🔗 Genera link", key="pr_gen_link"):
        url = genera_link_cattura_mobile(conn, paz_id, base_url, int(ttl))
        st.code(url, language="text")
        st.caption("Apri questo link sul telefono del paziente/operatore: guida flash+zoom+distanza, poi le foto tornano qui.")
    st.markdown("---")

    rows = _storico(conn, paz_id)
    if rows:
        st.markdown("#### Storico stime")
        for r in rows:
            data_s, s_od, s_os, asim = (r.get(k) if hasattr(r, "get") else r[i]
                                        for i, k in enumerate(["data_sessione","stima_od","stima_os","asimmetria"]))
            st.caption(f"📅 {data_s} · Stima OD {s_od:+.2f} D · OS {s_os:+.2f} D · Asimmetria {asim:.2f} D")
        st.markdown("---")

    st.markdown("#### Carica foto (o usa il link mobile sopra e poi carica qui il risultato)")
    c1, c2 = st.columns(2)
    with c1:
        foto_od = st.file_uploader("Foto riflesso OD", type=["jpg","jpeg","png"], key="photoref_od")
    with c2:
        foto_os = st.file_uploader("Foto riflesso OS", type=["jpg","jpeg","png"], key="photoref_os")

    data_s = st.date_input("Data sessione", datetime.date.today(), key="photoref_data")
    note = st.text_area("Note (distanza, illuminazione, collaborazione paziente)", "", height=68, key="photoref_note")

    if st.button("🔍 Analizza e salva", type="primary", key="photoref_analizza"):
        if not foto_od or not foto_os:
            st.warning("Carica entrambe le foto (OD e OS) prima di analizzare.")
            return
        try:
            img_od = Image.open(io.BytesIO(foto_od.read()))
            img_os = Image.open(io.BytesIO(foto_os.read()))
            stima_od = _analizza_riflesso(img_od, "OD")
            stima_os = _analizza_riflesso(img_os, "OS")
            asimmetria = round(abs(stima_od - stima_os), 2)
            _salva(conn, paz_id, data_s.isoformat(), stima_od, stima_os, asimmetria, note)
            st.success(f"Stima OD {stima_od:+.2f} D · OS {stima_os:+.2f} D · Asimmetria {asimmetria:.2f} D")
            if asimmetria >= 1.0:
                st.warning("⚠️ Asimmetria ≥1.00 D — consigliata verifica con refrazione oggettiva completa.")
            st.rerun()
        except Exception as e:
            st.error(f"Errore analisi immagine: {e}")
